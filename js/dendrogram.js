function loadClusterDataAndDisplayModal() {
    d3.csv("../data/Countries.csv", function(error, clusterData) {
        if (error) throw error;

        var modalContent = d3.select("#modal-content2");
        modalContent.html("");
        modalContent.append("span")
        .attr("class", "close")
        .html("&times;")
        .on("click", function() {
            document.getElementById('myModal2').style.display = 'none';
            console.log("222");
        });

        var clusterTable = modalContent.append("table").attr("class", "cluster-table");
        var clusterColumns = Array.from({ length: 12 }, (_, i) => "Cluster " + (i + 1));

        clusterTable.append("tr")
            .selectAll("th")
            .data(clusterColumns)
            .enter()
            .append("th")
            .text(function(d) { return d; });

            clusterTable.selectAll("tr.row")
            .data(clusterData.slice(0, 1)) // Берем только одну строку, так как кластеры одинаковые для всех стран
            .enter()
            .append("tr")
            .attr("class", "row")
            .selectAll("td")
            .data(function(row) {
                return clusterColumns.map(function(column) {
                    var clusterNumber = parseInt(column.split(" ")[1]);
                    var countriesInCluster = clusterData
                        .filter(function(country) {
                            return country['Cluster'] == clusterNumber;
                        })
                        .map(function(country) {
                            return country['Country Name'];
                        });
                    return { column: column, value: countriesInCluster.join('; ') };
                });
            })
            .enter()
            .append("td")
            .text(function(d) { return d.value; });
    });
}